/*
 * @Title: Wikipedia Menu Hover
 * 
 * Makes wikipedia menus hover and track with the page, e.g. https://imgur.com/a/N6kMzrg.
 *
 */
(function() {
  let toc = $('#toc');
  toc.css('position', 'fixed');
  toc.css('right', '10px');
  toc.css('top', '90px');
  toc.css('height', '80%');
  toc.css('overflow', 'auto');
  toc.css('display', 'block');
})();
