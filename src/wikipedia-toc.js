/*
 * @Title: Wikipedia Menu Hover
 * @Description: Makes wikipedia menus hover and track with the page.
 * @Image: https://i.imgur.com/mx6s5Ws.png
 *
 */
(function () {
  let toc = $('#toc');
  toc.css('position', 'fixed');
  toc.css('right', '10px');
  toc.css('top', '90px');
  toc.css('height', '80%');
  toc.css('overflow', 'auto');
  toc.css('display', 'block');
})();
